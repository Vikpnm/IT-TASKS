def check_technical_qualities():
    # Словник важливих якостей для технічних професій
    technical_qualities = {
        "аналітичне_мислення": "Здатність аналізувати складні проблеми",
        "увага_до_деталей": "Висока увага до деталей",
        "математичні_навички": "Хороші математичні здібності",
        "логічне_мислення": "Сильне логічне мислення",
        "технічний_склад_розуму": "Інтерес до того, як працюють речі",
        "вирішення_проблем": "Здатність вирішувати складні завдання",
        "терпіння": "Терпіння при роботі з технічними питаннями",
        "постійне_навчання": "Готовність вивчати нові технології"
    }

    print("\nЛаскаво просимо до Перевірки якостей для технічних професій!")
    print("Ця програма допоможе вам визначити, чи маєте ви якості, необхідні для технічної кар'єри.")
    print("\nВажливі якості для технічних професій:")
    
    # Показати всі якості
    for i, (quality, description) in enumerate(technical_qualities.items(), 1):
        print(f"{i}. {description}")

    while True:
        print("\nБажаєте перевірити, чи важлива певна якість? (так/ні)")
        response = input().lower()
        
        if response != 'так':
            break
            
        print("\nВведіть якість, яку хочете перевірити (наприклад, 'терпіння', 'логічне мислення'):")
        user_quality = input().lower()
        
        # Перевірка важливості якості
        found = False
        for quality, description in technical_qualities.items():
            if user_quality in quality.lower() or user_quality in description.lower():
                print(f"\n✓ Так! '{description}' є важливою якістю для технічних професій!")
                found = True
                break
                
        if not found:
            print("\n✗ Цю якість не знайдено у нашому списку основних технічних якостей.")
            print("Проте вона все ще може бути цінною в певних технічних ролях!")

if __name__ == "__main__":
    check_technical_qualities() 